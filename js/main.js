class Game {
  #id;
  name;
  #releaseDate;
  #genres;
  rating;

  constructor(id, name, releaseDate, genres, rating) {
    this.#id = id;
    this.name = name;
    this.#releaseDate = releaseDate;
    this.#genres = genres;
    this.rating = rating;
  }

  static loadDB(games, limit = null) {
    limit = limit ?? GAMES_DB.length;
    for (let i = 0; i < limit; i++) {
      const entry = GAMES_DB[i];
      const name = entry.name;
      const releaseDate = new Date(entry.first_release_date * 1000);
      let genres = [];
      if (entry.genres) {
        genres = entry.genres.map((genreID) => {
          return GENRES_DB.find((genre) => genre.id === genreID).name;
        });
      }
      const rating = entry.total_rating;
      const game = new Game(entry.id, name, releaseDate, genres, rating);
      games.push(game);
    }
  }

  get id() {
    return this.#id;
  }

  get genres() {
    let genres = "";
    for (const [i, genre] of this.#genres.entries()) {
      genres += genre;
      if (i !== this.#genres.length - 1) {
        genres += " | ";
      }
    }
    return genres;
  }

  get releaseDate() {
    return `${this.#releaseDate.toLocaleDateString()}`;
  }
}

class StorageController {
  static #gameListKey = "gameList";

  static loadGameList() {
    const stringList = localStorage.getItem(this.#gameListKey);
    let gameList = [];
    const JSONList = JSON.parse(stringList);
    if (JSONList) {
      gameList = JSONList.map((gameID) =>
        games.find((game) => game.id === gameID),
      );
    }
    return gameList;
  }

  static saveGameList(gameList = []) {
    const idList = gameList.map((game) => game.id);
    const JSONList = JSON.stringify(idList);
    localStorage.setItem(this.#gameListKey, JSONList);
  }
}

class OrderedListController {
  #elements;
  #orderedList;

  constructor(orderedList = []) {
    this.#orderedList = orderedList;
    this.#elements = new Set(this.#orderedList);
  }

  get list() {
    return this.#orderedList;
  }

  get length() {
    return this.#elements.size;
  }

  add(game, order = 1) {
    let updated = false;
    const currentLength = this.length;
    this.#elements.add(game);
    const newLength = this.length;
    if (currentLength != newLength) {
      if (order < 1) {
        this.#orderedList.unshift(game);
      } else if (order > newLength) {
        this.#orderedList.push(game);
      } else {
        const listTail = this.#orderedList.splice(order - 1);
        this.#orderedList.push(game);
        this.#orderedList.push(...listTail);
      }
      updated = true;
      StorageController.saveGameList(this.#orderedList);
    }
    return updated;
  }

  clear() {
    this.#elements.clear();
    this.#orderedList = [];
    StorageController.saveGameList();
  }
}

